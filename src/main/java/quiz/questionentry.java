    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
String question=jTextArea1.getText();
String option1=jTextField1.getText();
String option2=jTextField2.getText();
String option3=jTextField3.getText();
String option4=jTextField4.getText();
String correct_ans=jTextField5.getText();
String marks=jTextField6.getText();
int mark=Integer.parseInt(marks);

 int id;
 int var=0;
   
     try
    {
         SqlConnect c=new SqlConnect();
         Statement str1=c.con.createStatement();
         ResultSet rs=str1.executeQuery("select max(user) from question");
         while(rs.next())
         {
              id=rs.getInt("max(user)");
              var=id+1;
         }
         PreparedStatement str=c.con.prepareStatement("insert into question values(?,?,?,?,?,?,?,?)");
         str.setInt(1,var);
         str.setString(2,question);
         str.setString(3,option1);
         str.setString(4,option2);
         str.setString(5,option3);
         str.setString(6,option4);
         str.setString(7,correct_ans);
         str.setInt(8,mark);
         int b=str.executeUpdate();
         if(b==1)
         {
             JOptionPane.showMessageDialog(this,"Record Updated");
         }
         else
         {
             JOptionPane.showMessageDialog(this,"Record Not Updated");
         }
         questionentry c2=new questionentry();
         c2.setVisible(true);
         c2.setSize(676, 480);
         this.dispose();
    
    }
     catch(Exception e)
     {
         System.out.println("Exception "+e);
     }
        
    }//GEN-LAST:event_jButton1ActionPerformed

